public class Engine {
	
	private double engine_capacity; // объем двигателя
	private int engine_speed;       // обороты в минуту
	
	public double getEngine_capacity() {
		return engine_capacity;
	}
	public void setEngine_capacity(double engine_capacity) {
		this.engine_capacity = engine_capacity;
	}
	public int getEngine_speed() {
		return engine_speed;
	}
	public void setEngine_speed(int engine_speed) {
		this.engine_speed = engine_speed;
	}
	
	
}
